const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const backendDist = path.join(repoRoot, 'backend', 'dist');
const functionsDir = path.join(repoRoot, 'netlify', 'functions', 'server');
const dataDir = path.join(repoRoot, 'data');

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

function cleanDir(dir) {
  if (fs.existsSync(dir)) fs.rmSync(dir, { recursive: true, force: true });
  ensureDir(dir);
}

function copyRecursive(src, dest) {
  if (!fs.existsSync(src)) return;
  const st = fs.statSync(src);
  if (st.isDirectory()) {
    ensureDir(dest);
    for (const name of fs.readdirSync(src)) {
      copyRecursive(path.join(src, name), path.join(dest, name));
    }
  } else {
    ensureDir(path.dirname(dest));
    fs.copyFileSync(src, dest);
  }
}

// prepare clean functions folder
cleanDir(functionsDir);

// copy entire backend dist (preserve structure)
copyRecursive(backendDist, functionsDir);

// copy backend package.json for fallback resolution
const backendPkg = path.join(repoRoot, 'backend', 'package.json');
if (fs.existsSync(backendPkg)) {
  try {
    fs.copyFileSync(backendPkg, path.join(functionsDir, 'package.json'));
  } catch (e) {
    /* ignore */
  }
}

// copy data (optional, note: ephemeral on Netlify)
if (fs.existsSync(dataDir)) {
  copyRecursive(dataDir, path.join(functionsDir, 'data'));
}

// locate compiled handler inside the copied dist tree (returns path relative to functionsDir)
function findHandlerRelative(startDir) {
  const rx = /netlify-handler\.(js|cjs|mjs)$/i;
  const queue = [startDir];
  while (queue.length) {
    const dir = queue.shift();
    let entries;
    try {
      entries = fs.readdirSync(dir, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const e of entries) {
      const full = path.join(dir, e.name);
      if (e.isDirectory()) queue.push(full);
      else if (rx.test(e.name))
        return path.relative(functionsDir, full).replace(/\\/g, '/');
    }
  }
  return null;
}

// fallback: try common index candidates inside the copied dist
function findIndexRelative(startDir) {
  const candidates = [
    path.join(startDir, 'index.js'),
    path.join(startDir, 'server.js'),
    path.join(startDir, 'dist', 'index.js'),
  ];
  for (const c of candidates) {
    const dest = path.relative(functionsDir, c).replace(/\\/g, '/');
    if (fs.existsSync(c)) return dest;
  }
  return null;
}

let requirePathRel = null;

// prefer handler found in copied dist
requirePathRel = findHandlerRelative(backendDist);
if (requirePathRel) {
  requirePathRel = './' + requirePathRel;
  console.log('Using compiled handler at', requirePathRel);
} else {
  const idx = findIndexRelative(backendDist);
  if (idx) {
    requirePathRel = './' + idx;
    console.log('Using index fallback at', requirePathRel);
  }
}

// as last resort, use first .js at functionsDir root
if (!requirePathRel) {
  const rootJs = fs.existsSync(functionsDir)
    ? fs.readdirSync(functionsDir).find((f) => f.endsWith('.js'))
    : null;
  if (rootJs) requirePathRel = './' + rootJs;
}

if (!requirePathRel) {
  console.warn('No candidate backend entry found in', functionsDir);
}

// write generated server.js inside functionsDir so relative requires resolve
const serverEntry = path.join(functionsDir, 'server.js');
const rp = (requirePathRel || './index.js').replace(/\\/g, '/');

const wrapper = `// Auto-generated by prepare-netlify-function.js
// requires: ${rp}
const requirePath = '${rp}';
let mod;
try {
  mod = require(requirePath);
} catch (err) {
  console.error('Error requiring backend module:', err && err.stack ? err.stack : err);
  exports.handler = async () => ({
    statusCode: 500,
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ error: 'server_require_error', message: String(err && err.message ? err.message : err) }),
  });
  return;
}

let handler = mod.handler || (mod.default && mod.default.handler);

if (typeof handler === 'function') {
  exports.handler = handler;
} else if (mod && (mod.app || (mod.default && mod.default.app))) {
  const expressApp = mod.app || (mod.default && mod.default.app);
  try {
    const serverless = require('serverless-http');
    exports.handler = serverless(expressApp);
  } catch (e) {
    console.error('serverless-http wrap failed:', e && e.stack ? e.stack : e);
    exports.handler = async () => ({
      statusCode: 500,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ error: 'serverless_wrap_error', message: String(e && e.message ? e.message : e) }),
    });
  }
} else if (typeof mod === 'function') {
  exports.handler = mod;
} else {
  console.error('No handler found in required module:', requirePath);
  exports.handler = async () => ({
    statusCode: 500,
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ error: 'no_handler', message: 'No handler found in backend build.' }),
  });
}
`;
fs.writeFileSync(serverEntry, wrapper, 'utf8');
console.log('Wrote Netlify entry:', serverEntry, '-> requires', rp);

console.log('Prepared Netlify function at', functionsDir);
